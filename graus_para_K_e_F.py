#converter graus para kelvin e farenheit 

#ler valor em graus
graus = float(input("insira a temperatura em graus: "))
 
#converter graus para kelvin
kelvin = graus + 273.15 

#imprimir valor em kelvin
print("a temperatura em Kelvin é:", kelvin)

#converter graus para farenheit
F = graus*1.8 + 32 

#imprimir valor em farenheit
print("a temperatura em Farenheit é:",F)
